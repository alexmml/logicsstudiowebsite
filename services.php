                    <h1>Process to Success</h1>
                    <h2>Combine Agile methods and Gantt charts to make the project to the real successful accomplish.</h2>
                </div>
                <h4>Logics Studio knows how the development process is a hard thing to do. The collaboration often is difficult and is hard to plan because everyone has a lot of things to do. So we plan a method that combine the best things of Agile and Gantt process to really get know how the project is.</h4>
                <br />
                <br />
                <table class="table-1">
                    <tr>
                        <td colspan="2" style="text-align: center">Agile Vs Gantt</td>
                    </tr>
                    <tr>
                        <td colspan="2" style="text-align: center">The Pros</td>
                    </tr>
                    <tr>
                        <td>It is More flexible</td>
                        <td>It creates a picture of complexity and it can be highly visible</td>
                    </tr>
                    <tr>
                        <td>Make products to market faster</td>
                        <td>Organize thoughts and "should" help to realistic time frames</td>
                    </tr>
                    <tr>
                        <td>It involves better communication</td>
                        <td>Demostrate what we are doing in the moment</td>
                    </tr>
                    <tr>
                        <td colspan="2" style="text-align: center">The Cons</td>
                    </tr>
                    <tr>
                        <td>It is very Hard to predict</td>
                        <td>It can become extraordinarily complex</td>
                    </tr>
                    <tr>
                        <td>Final product is not release first</td>
                        <td>The size of bars does not indicate the amount of work</td>
                    </tr>
                    <tr>
                        <td>Documentation get left behind</td>
                        <td>They need to be updated constantly</td>
                    </tr>
