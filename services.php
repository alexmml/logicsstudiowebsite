                <div class="notes">
                    <h5>Product Backlog</h5>
                    <p>Is all the desired product to develop. In this process we collect all raw materials and define the desired objective.</p>
                    <h5>Gantt Chart</h5>
                    <p>Is where we define all the milestones and scrums. It is a very important step to define all the possibilities. It also define the start and the possible ending. With Agile Methods fix the common error on when the milestone will end.</p>
                    <h5>Scrum process</h5>
                    <p>Each scrum has his own sprints. This is the development process. This process garanties that the work is being develop according to your needs. It always near to the potencially release product for the milestone. But if it needs more attention because a change, the Gantt chart will be move to.</p>
                    <h5>Release</h5>
                    <p>It the final solution ready to QA testing.</p>
                    <h5>Document</h5>
                    <p>When the final solution is ready to go, it will be well documented for production.</p>
